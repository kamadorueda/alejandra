// Import all content files dynamically
// Vite can statically analyze this and apply manualChunks for code splitting
const contentModules = import.meta.glob<{ default: string }>(
  './content/*.ts',
  { eager: false }
);

export const getRandomFile = async (): Promise<{ path: string; content: string }> => {
  const paths = Object.keys(fileHashes);
  const path = paths[Math.floor(Math.random() * paths.length)];
  const hash = fileHashes[path];
  const modulePath = `./content/${hash}.ts`;
  const mod = await contentModules[modulePath]?.();
  return { path, content: mod?.default || '' };
};
